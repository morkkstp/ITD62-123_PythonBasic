# ทุกข้ออ้างอิง list จากข้อที่ 1
#1
listname = ["Jessy", "Ammy", "Luzy", "Jenny", "John"]
print("ข้อที่ 1 =",listname)

# 2
print("ข้อที่ 2 =",listname[1])

# 3
listname.pop(3)
print("ข้อที่ 3 =",listname)

# 4
listname.append("BigBoss")
print("ข้อที่ 4 =",listname)

# 5
listname.insert(3, "Jenny")
print("ข้อที่ 5 =",listname[2:5])

# 6
print("ข้อที่ 6 =",listname[:2])
